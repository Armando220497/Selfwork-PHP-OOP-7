require_once 'Ironman.php';
require_once 'ParteSuperiore.php';
require_once 'ParteInferiore.php';

//opzioni per la parte superiore e inferiore
$parteSuperioreOptions = [new Missili(), new Mitra()];
$parteInferioreOptions = [new Propulsori(), new Salto()];

//esercito di Ironman 
$numeroIronman = 10;
for ($i = 1; $i <= $numeroIronman; $i++) {
    echo "Ironman: $i \n";
    $randomParteSuperiore = $parteSuperioreOptions[array_rand($parteSuperioreOptions)];
    $randomParteInferiore = $parteInferioreOptions[array_rand($parteInferioreOptions)];


    $ironman = new Ironman($randomParteInferiore, $randomParteSuperiore);
    $ironman->attaccare();
    $ironman->volare();
    echo "\n";
}

echo "Ironman creati: " . Ironman::$counter;
